/* Default language */
let currentLang = 'zh-CN';

function getText(key) {
  return i18n[currentLang][key] || i18n['zh-CN'][key] || key;
}

function applyLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  localStorage.setItem('atlas-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = getText(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = text;
    } else if (el.tagName === 'SELECT') {
      // handled by option children
    } else if (el.tagName === 'OPTION') {
      el.textContent = text;
    } else {
      el.innerHTML = text;
    }
  });

  // Update lang switcher active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  // Update body font based on language
  if (lang === 'en') {
    document.body.style.fontFamily = "var(--font-en)";
  } else {
    document.body.style.fontFamily = "var(--font-cn)";
  }
}

function initLanguage() {
  const saved = localStorage.getItem('atlas-lang');
  const lang = saved || 'zh-CN';
  applyLanguage(lang);
}
